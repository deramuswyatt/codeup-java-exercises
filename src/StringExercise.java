public class StringExercise {
    public static void main(String[] args) {
        String rebel = "We don't need no education\n" +
        "We don't need no thought control";
        System.out.println(rebel);

        String s = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println(s);

        String windows = "In windows, the main drive is usually C:\\";
        System.out.println(windows);

        String backlash = "I can do backslashes \\, double backslashes \\\\,\n" +
                "and the amazing triple backslash \\\\\\!";
        System.out.println(backlash);
    }
}
